CREATE TABLE IF NOT EXISTS credit (
            BAD INTEGER,
            LOAN INTEGER,
            MORTDUE INTEGER,
            VALUE INTEGER,
            REASON VARCHAR(255),
            JOB VARCHAR(255),
            YOJ DOUBLE,
            DEROG INTEGER,
            DELINQ INTEGER,
            CLAGE DOUBLE,
            NINQ INTEGER,
            CLNO INTEGER,
            DEBTINC DOUBLE)